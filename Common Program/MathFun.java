public class MathFun {
    public static void main(String[] args) {
        int a=2;
        int b=4;

        System.out.println("the minimum value is ="+ Math.min(a,b));
        System.out.println("the maximum value is ="+ Math.max(a,b));
        System.out.println("the power value is ="+ Math.pow(a,b));
        System.out.println("the squareroot is ="+ Math.sqrt(b));
        System.out.println("the absolute value is ="+ Math.abs(a));

    }
}
